package nextStep.chapter02;

public class Calculator {
	public int add(int a, int b) {
		return a+b;
	}
	
	public int minus(int a, int b) {
		return a-b;
	}
	
	public int multiple(int a, int b) {
		return a*b;
	}
	
	public int divide(int a, int b) {
		return a/b;
	}
	
	public int remain(int a, int b) {
		return a%b;
	}
	
}
